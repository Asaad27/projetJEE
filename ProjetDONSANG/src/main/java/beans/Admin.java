package beans;


public class Admin {
    private int idAdmin;
    private String nomAdmin;
    private String prenomAdmin;
    private String cinAdmin;
    private String emailAdmin;
    private String passwordAdmin;
    private String teleAdmin;

    public int getIdAdmin() {
        return idAdmin;
    }

    public void setIdAdmin(int idAdmin) {
        this.idAdmin = idAdmin;
    }

    public String getNomAdmin() {
        return nomAdmin;
    }

    public void setNomAdmin(String nomAdmin) {
        this.nomAdmin = nomAdmin;
    }

    public String getPrenomAdmin() {
        return prenomAdmin;
    }

    public void setPrenomAdmin(String prenomAdmin) {
        this.prenomAdmin = prenomAdmin;
    }

    public String getCinAdmin() {
        return cinAdmin;
    }

    public void setCinAdmin(String cinAdmin) {
        this.cinAdmin = cinAdmin;
    }

    public String getEmailAdmin() {
        return emailAdmin;
    }

    public void setEmailAdmin(String emailAdmin) {
        this.emailAdmin = emailAdmin;
    }

    public String getPasswordAdmin() {
        return passwordAdmin;
    }

    public void setPasswordAdmin(String passwordAdmin) {
        this.passwordAdmin = passwordAdmin;
    }

    public String getTeleAdmin() {
        return teleAdmin;
    }

    public void setTeleAdmin(String teleAdmin) {
        this.teleAdmin = teleAdmin;
    }
}